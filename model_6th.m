function [Time,I,R]= model_6th(B1,B2,gam,sus2,infected,exposed,removed,tot)  
    format long
        t0 = 1;
        h = 1;
        tEnd = size(infected,1);
        N = (tEnd-t0)/h ;
        Time = (t0:h:tEnd);
        S = zeros(N+1,1);
        E = zeros(N+1,1);
        I = zeros(N+1,1);
        R = zeros(N+1,1);
        S(1) = sus2(1)/tot;
        E(1) = exposed(1)/tot;
        I(1) = infected(1)/tot;
        R(1) = removed(1)/tot;

             for i = 1:N
                 T = Time(i);
                 k1 = h* fun_s(T,B1,B2,S(i),E(i),I(i),R(i),tot);
                 l1 = h* fun_e(T,B1,B2,S(i),E(i),I(i),R(i),tot);
                 m1 = h* fun_i(T,B1,B2,S(i),E(i),I(i),R(i),tot,gam);
                 n1 = h* fun_r(T,B1,B2,S(i),E(i),I(i),R(i),tot,gam);

                 k2 = fun_s(T+(1/4)*h,B1,B2,S(i)+(1/4)*k1*h,E(i)+(1/4)*l1*h,I(i)+(1/4)*m1*h,R(i)+(1/4)*n1*h,tot) ;
                 l2 = fun_e(T+(1/4)*h,B1,B2,S(i)+(1/4)*k1*h,E(i)+(1/4)*l1*h,I(i)+(1/4)*m1*h,R(i)+(1/4)*n1*h,tot);
                 m2 = fun_i(T+(1/4)*h,B1,B2,S(i)+(1/4)*k1*h,E(i)+(1/4)*l1*h,I(i)+(1/4)*m1*h,R(i)+(1/4)*n1*h,tot,gam);
                 n2 = fun_r(T+(1/4)*h,B1,B2,S(i)+(1/4)*k1*h,E(i)+(1/4)*l1*h,I(i)+(1/4)*m1*h,R(i)+(1/4)*n1*h,tot,gam);

                 k3 = fun_s(T+(1/4)*h,B1,B2,S(i)+(1/8)*k1*h+(1/8)*k2*h,E(i)+(1/8)*l1*h+(1/8)*l2*h,I(i)+(1/8)*m1*h+(1/8)*m2*h,R(i)+(1/8)*n1*h+(1/8)*n2*h,tot);
                 l3 = fun_e(T+(1/4)*h,B1,B2,S(i)+(1/8)*k1*h+(1/8)*k2*h,E(i)+(1/8)*l1*h+(1/8)*l2*h,I(i)+(1/8)*m1*h+(1/8)*m2*h,R(i)+(1/8)*n1*h+(1/8)*n2*h,tot);
                 m3 = fun_i(T+(1/4)*h,B1,B2,S(i)+(1/8)*k1*h+(1/8)*k2*h,E(i)+(1/8)*l1*h+(1/8)*l2*h,I(i)+(1/8)*m1*h+(1/8)*m2*h,R(i)+(1/8)*n1*h+(1/8)*n2*h,tot,gam);
                 n3 = fun_r(T+(1/4)*h,B1,B2,S(i)+(1/8)*k1*h+(1/8)*k2*h,E(i)+(1/8)*l1*h+(1/8)*l2*h,I(i)+(1/8)*m1*h+(1/8)*m2*h,R(i)+(1/8)*n1*h+(1/8)*n2*h,tot,gam);

                 k4 = fun_s(T+(h/2),B1,B2,S(i)-(1/2)*k2*h+k3*h,E(i)-(1/2)*l2*h+l3*h,I(i)-(1/2)*m2*h+m3*h,R(i)-(1/2)*n2*h+n3*h,tot);
                 l4 = fun_e(T+(h/2),B1,B2,S(i)-(1/2)*k2*h+k3*h,E(i)-(1/2)*l2*h+l3*h,I(i)-(1/2)*m2*h+m3*h,R(i)-(1/2)*n2*h+n3*h,tot);
                 m4 = fun_i(T+(h/2),B1,B2,S(i)-(1/2)*k2*h+k3*h,E(i)-(1/2)*l2*h+l3*h,I(i)-(1/2)*m2*h+m3*h,R(i)-(1/2)*n2*h+n3*h,tot,gam);
                 n4 = fun_r(T+(h/2),B1,B2,S(i)-(1/2)*k2*h+k3*h,E(i)-(1/2)*l2*h+l3*h,I(i)-(1/2)*m2*h+m3*h,R(i)-(1/2)*n2*h+n3*h,tot,gam);

                 k5 = fun_s(T+(3/4)*h,B1,B2,S(i)+(3/16)*k1*h+(9/8)*k4*h,E(i)+(3/16)*l1*h+(9/8)*l4*h,I(i)+(3/16)*m1*h+(9/8)*m4*h,R(i)+(3/16)*n1*h+(9/8)*n4*h,tot);
                 l5 = fun_e(T+(3/4)*h,B1,B2,S(i)+(3/16)*k1*h+(9/8)*k4*h,E(i)+(3/16)*l1*h+(9/8)*l4*h,I(i)+(3/16)*m1*h+(9/8)*m4*h,R(i)+(3/16)*n1*h+(9/8)*n4*h,tot);
                 m5 = fun_i(T+(3/4)*h,B1,B2,S(i)+(3/16)*k1*h+(9/8)*k4*h,E(i)+(3/16)*l1*h+(9/8)*l4*h,I(i)+(3/16)*m1*h+(9/8)*m4*h,R(i)+(3/16)*n1*h+(9/8)*n4*h,tot,gam);
                 n5 = fun_r(T+(3/4)*h,B1,B2,S(i)+(3/16)*k1*h+(9/8)*k4*h,E(i)+(3/16)*l1*h+(9/8)*l4*h,I(i)+(3/16)*m1*h+(9/8)*m4*h,R(i)+(3/16)*n1*h+(9/8)*n4*h,tot,gam);

                 k6 = fun_s(T+h,B1,B2,S(i)-(3/7)*k1*h+(2/7)*k2*h+(12/7)*k3*h-(12/7)*k4*h+(8/7)*k5*h,E(i)-(3/7)*l1*h+(2/7)*l2*h+(12/7)*l3*h-(12/7)*l4*h+(8/7)*l5*h,I(i)-(3/7)*m1*h+(2/7)*m2*h+(12/7)*m3*h-(12/7)*m4*h+(8/7)*m5*h,R(i)-(3/7)*n1*h+(2/7)*n2*h+(12/7)*n3*h-(12/7)*n4*h+(8/7)*n5*h,tot);
                 l6 = fun_e(T+h,B1,B2,S(i)-(3/7)*k1*h+(2/7)*k2*h+(12/7)*k3*h-(12/7)*k4*h+(8/7)*k5*h,E(i)-(3/7)*l1*h+(2/7)*l2*h+(12/7)*l3*h-(12/7)*l4*h+(8/7)*l5*h,I(i)-(3/7)*m1*h+(2/7)*m2*h+(12/7)*m3*h-(12/7)*m4*h+(8/7)*m5*h,R(i)-(3/7)*n1*h+(2/7)*n2*h+(12/7)*n3*h-(12/7)*n4*h+(8/7)*n5*h,tot);
                 m6 = fun_i(T+h,B1,B2,S(i)-(3/7)*k1*h+(2/7)*k2*h+(12/7)*k3*h-(12/7)*k4*h+(8/7)*k5*h,E(i)-(3/7)*l1*h+(2/7)*l2*h+(12/7)*l3*h-(12/7)*l4*h+(8/7)*l5*h,I(i)-(3/7)*m1*h+(2/7)*m2*h+(12/7)*m3*h-(12/7)*m4*h+(8/7)*m5*h,R(i)-(3/7)*n1*h+(2/7)*n2*h+(12/7)*n3*h-(12/7)*n4*h+(8/7)*n5*h,tot,gam);
                 n6 = fun_r(T+h,B1,B2,S(i)-(3/7)*k1*h+(2/7)*k2*h+(12/7)*k3*h-(12/7)*k4*h+(8/7)*k5*h,E(i)-(3/7)*l1*h+(2/7)*l2*h+(12/7)*l3*h-(12/7)*l4*h+(8/7)*l5*h,I(i)-(3/7)*m1*h+(2/7)*m2*h+(12/7)*m3*h-(12/7)*m4*h+(8/7)*m5*h,R(i)-(3/7)*n1*h+(2/7)*n2*h+(12/7)*n3*h-(12/7)*n4*h+(8/7)*n5*h,tot,gam);

                 S(i+1) = S(i) + (1/90) * (7*k1 + 32*k2 + 12*k4 + 32*k5 + 7*k6);
                 E(i+1) = E(i) + (1/90) * (7*l1 + 32*l2 + 12*l4 + 32*l5 + 7*l6);
                 I(i+1) = I(i) + (1/90) * (7*m1 + 32*m2 + 12*m4 + 32*m5 + 7*m6);
                 R(i+1) = R(i) + (1/90) * (7*n1 + 32*n2 + 12*n4 + 32*n5 + 7*n6);

             end
    end
    
